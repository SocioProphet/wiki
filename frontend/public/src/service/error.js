export const ErrorService = new class {
    constructor() {
        this.lang = getLocale();
    }

    map(validation) {
        if(!validation) {
            return validation;
        }

        for(let key in validation) {
            if(validation.hasOwnProperty(key)) {
                validation[key] = this.mapError(validation[key]);
            }
        }

        return validation;
    }

    mapError(error) {
        if(errs[this.lang].hasOwnProperty(error)) {
            return errs[this.lang][error];
        }

        return error;
    }

    setLocale(locale) {
        this.lang = locale.toLowerCase();
    }
};
